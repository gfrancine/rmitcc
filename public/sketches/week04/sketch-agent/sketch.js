document.title = "W4 - Coding Agents";

let obstacles = [];
let numObstacles = 50;
let message = "TRUTH IS OFTEN HIDDEN UNDER LAYERS.";

function setup() {
  createCanvas(600, 400);
  rectMode(CENTER);
  textAlign(CENTER, CENTER);
  textSize(32);
  textFont("Helvetica");

  // Create random squares across the canvas
  for (let i = 0; i < numObstacles; i++) {
    obstacles.push(new Obstacle(random(width), random(height)));
  }
}

function draw() {
  background(245);

  // 1. Draw the "Hidden" Text
  fill(50);
  noStroke();
  text(message, width / 2, height / 2);

  // 2. Update and Draw Obstacles
  for (let obs of obstacles) {
    obs.update();
    obs.display();
  }
}

class Obstacle {
  constructor(x, y) {
    this.home = createVector(x, y);
    this.pos = createVector(x, y);
    this.size = random(30, 60);
    this.vel = createVector(0, 0);
    this.friction = 0.9; // Slows movement down
    this.repelDist = 100; // Sensitivity radius
  }

  update() {
    let mouse = createVector(mouseX, mouseY);
    let distToMouse = p5.Vector.dist(this.pos, mouse);

    if (distToMouse < this.repelDist) {
      // Create a force pointing away from the mouse
      let force = p5.Vector.sub(this.pos, mouse);
      force.setMag(2); // Speed of the push
      this.vel.add(force);
    } else {
      // Gently return home if the mouse is far away
      let returnForce = p5.Vector.sub(this.home, this.pos);
      returnForce.mult(0.05);
      this.vel.add(returnForce);
    }

    this.pos.add(this.vel);
    this.vel.mult(this.friction);
  }

  display() {
    fill(0); // Black squares
    rect(this.pos.x, this.pos.y, this.size, this.size);
  }
}
